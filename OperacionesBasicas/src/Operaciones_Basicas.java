public class Operaciones_Basicas {
	
	public static void main(String[] args) {
		
	int n1 = 10;
	int n2 = 5;	

	System.out.println("suma es igual a "+(n1+n2));
	
	System.out.println("resta es igual a "+(n1-n2));
	
	System.out.println("multiplicación es igual a "+(n1*n2));
	
	System.out.println("división es igual a "+(n1/n2));
	
	}
}


